name = "nicOle herRera"
# uppercases the first letter of every word
print(name.title())

'''
Methods are actions that python can perform on a piece of data. title(), upper(), lower() are all methods.
The period tells python to invoke the method on the variable before it. braces are present because some methods require more 
info to perform their action. 

'''
#uppercases every letter in the string
print(name.upper())

#lowercases every letter in the string
#useful for when needing to standarize the data being stored for use in later in the code. 
print(name.lower())